# ==================== GIGACHAT — МЯГКИЙ ПРОМПТ ====================
async def handle_gigachat(message: types.Message):
    if not GIGACHAT_AUTH_KEY:
        await message.answer("❌ Ключ GigaChat не настроен.")
        return

    try:
        with open("rules.txt", "r", encoding="utf-8") as f:
            rules_content = f.read().strip()
    except FileNotFoundError:
        rules_content = "Правила не найдены."

    user_text = message.text

    system_prompt = f"""
Ты — очень доброжелательный, спокойный и поддерживающий коуч по банкротству физических лиц. 
Ты говоришь тепло, с пониманием и заботой, как опытный старший товарищ.

**Стиль общения:**
- Начинай с эмпатии ("Я понимаю...", "Это совершенно нормально...", "Многие в такой ситуации чувствуют то же самое...")
- Говори простым, живым, человеческим языком
- Будь мягким, ободряющим и поддерживающим
- Избегай сухого и жёсткого тона

**Правило:**
Используй информацию только из файла rules.txt.

=== ПРАВИЛА ИЗ rules.txt ===
{rules_content}
=== КОНЕЦ ПРАВИЛ ===

Структура ответа:
1. Эмпатичное начало
2. Прямой ответ
3. Расчёт конкурсной массы простым текстом
4. Подробный разбор защиты имущества
5. Мягкие уточняющие вопросы

Отвечай тепло и по-человечески. Не используй LaTeX.
"""

    try:
        async with aiohttp.ClientSession() as session:
            async with session.post(
                "https://ngw.devices.sberbank.ru:9443/api/v2/oauth",
                headers={"Authorization": f"Basic {GIGACHAT_AUTH_KEY}", "Content-Type": "application/x-www-form-urlencoded", "Accept": "application/json", "RqUID": "00000000-0000-0000-0000-000000000000"},
                data="scope=GIGACHAT_API_PERS",
                ssl=False
            ) as resp:
                token_data = await resp.json()
                access_token = token_data.get("access_token")

            if not access_token:
                await message.answer("❌ Не удалось получить токен.")
                return

            async with session.post(
                "https://gigachat.devices.sberbank.ru/api/v1/chat/completions",
                headers={"Authorization": f"Bearer {access_token}", "Content-Type": "application/json"},
                json={
                    "model": "GigaChat",
                    "messages": [{"role": "system", "content": system_prompt}, {"role": "user", "content": user_text}],
                    "temperature": 0.5,
                    "max_tokens": 3000
                },
                ssl=False
            ) as resp:
                data = await resp.json()
                ai_reply = data["choices"][0]["message"]["content"]
                await message.answer(ai_reply, parse_mode="Markdown")

    except Exception as e:
        await message.answer(f"❌ Ошибка GigaChat:\n{str(e)[:600]}")

