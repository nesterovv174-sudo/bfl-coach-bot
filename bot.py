import logging
import asyncio
import os
from aiogram import Bot, Dispatcher, types
from aiogram.filters import Command
from aiogram.types import ReplyKeyboardMarkup, KeyboardButton

API_TOKEN = os.getenv("API_TOKEN")

bot = Bot(token=API_TOKEN)
dp = Dispatcher()

# ==================== ГЛАВНОЕ МЕНЮ ====================
main_menu = ReplyKeyboardMarkup(
    keyboard=[
        [KeyboardButton(text="📋 Возражения"), KeyboardButton(text="📞 Скрипты")],
        [KeyboardButton(text="⚡ Быстрый ответ"), KeyboardButton(text="👋 Приветствие")],
    ],
    resize_keyboard=True
)

# ==================== МЕНЮ ВОЗРАЖЕНИЙ ====================
vozrazheniya_menu = ReplyKeyboardMarkup(
    keyboard=[
        [KeyboardButton(text="💰 Дорого / Цена"), KeyboardButton(text="🏠 Заберут квартиру")],
        [KeyboardButton(text="🤔 Нужно подумать"), KeyboardButton(text="😟 Боюсь последствий")],
        [KeyboardButton(text="💸 Нет денег"), KeyboardButton(text="📞 Коллекторы звонят")],
        [KeyboardButton(text="🆓 Сам через МФЦ"), KeyboardButton(text="🔄 Уже пробовал")],
        [KeyboardButton(text="🚫 Не доверяю"), KeyboardButton(text="🙈 Стыдно / что скажут")],
        [KeyboardButton(text="⏰ Времени нет"), KeyboardButton(text="⚖️ Уже в суде")],
        [KeyboardButton(text="🔙 Назад в главное меню")],
    ],
    resize_keyboard=True
)

# ==================== МЕНЮ СКРИПТОВ ====================
skripty_menu = ReplyKeyboardMarkup(
    keyboard=[
        [KeyboardButton(text="📞 Скрипт первого разговора")],
        [KeyboardButton(text="🔙 Назад в главное меню")],
    ],
    resize_keyboard=True
)

@dp.message(Command("start"))
async def start_cmd(message: types.Message):
    await message.answer(
        "👋 Добро пожаловать в AI-коуч для менеджеров по БФЛ!\n\n"
        "Выберите нужный раздел:",
        reply_markup=main_menu
    )

@dp.message()
async def handle_message(message: types.Message):
    text = message.text

    if text == "📋 Возражения":
        await message.answer("Выберите возражение клиента:", reply_markup=vozrazheniya_menu)

    elif text == "📞 Скрипты":
        await message.answer("Выберите скрипт:", reply_markup=skripty_menu)

    elif text == "⚡ Быстрый ответ":
        await message.answer(
            "⚡ Режим быстрого ответа включён.\n\n"
            "Просто скопируйте и отправьте сообщение клиента — я предложу 2–3 варианта ответа.",
            reply_markup=main_menu
        )
        return

    elif text == "👋 Приветствие":
        await message.answer("👋 Добро пожаловать! Используйте меню для работы с клиентами.", reply_markup=main_menu)

    elif text == "🔙 Назад в главное меню":
        await message.answer("Главное меню:", reply_markup=main_menu)

    # ==================== СКРИПТ ПЕРВОГО РАЗГОВОРА ====================
    elif text == "📞 Скрипт первого разговора":
        reply = (
            "📞 **Скрипт первого разговора**\n\n"
            "Добрый день / вечер, {ИМЯ КЛИЕНТА}?\n\n"
            "— Это [ваше имя], я помощник юриста компании «БанкротствоГрупп». Вы недавно общались с моей коллегой (Имя сотрудника кол-центра), она передала мне, что у вас сумма кредитов составляет ………… т.р. и вам интересно списание данной суммы. Всё верно?\n\n"
            "Пауза для ответа клиента.\n\n"
            "— Отлично! Мы как раз специализируемся на списании всех кредитов и долгов и уже многим нашим гражданам помогли. Сейчас я уточню у вас пару вопросов, это займёт 5 минут. Удобно сейчас?\n\n"
            "Но перед этим расскажите, из чего эта сумма состоит (банки, МФО, долги по ЖКХ)?"
        )
        await message.answer(reply, parse_mode="Markdown", reply_markup=skripty_menu)

    # ==================== УЛУЧШЕННЫЕ ВОЗРАЖЕНИЯ ====================
    elif text == "💰 Дорого / Цена":
        reply = (
            "💰 **Дорого / Цена**\n\n"
            "**Вариант 1 (мягкий и понятный):**\n«Понимаю вас очень хорошо. Сейчас действительно каждая копейка на счету, и любая дополнительная трата вызывает вопросы. При этом каждый месяц на вас продолжают начисляться проценты и пени, которые часто съедают гораздо больше. Мы помогаем остановить этот рост уже в первые недели и предлагаем гибкую рассрочку. Давайте вместе посчитаем, как это будет выглядеть именно в вашем случае?»\n\n"
            "**Вариант 2 (с акцентом на результат):**\n«Согласен, сумма на первый взгляд кажется большой. Но давайте посмотрим по-другому: если ничего не делать, через год переплата будет значительно выше. Наша задача — полностью закрыть все долги и дать вам спокойную жизнь. Многие клиенты отмечают, что уже через месяц чувствуют огромное облегчение. Готовы посмотреть варианты оплаты?»"
        )

    elif text == "🏠 Заберут квартиру":
        reply = (
            "🏠 **Заберут квартиру**\n\n"
            "**Вариант 1:**\n«Это один из самых частых и понятных страхов. Хочу вас сразу успокоить: единственное жильё, в котором вы прописаны и проживаете, по закону полностью защищено и не может быть реализовано (кроме случая ипотеки). Мы всегда в начале проводим подробный анализ имущества, чтобы вы точно знали, что будет.»\n\n"
            "**Вариант 2:**\n«Я вас прекрасно понимаю — эта мысль действительно пугает. На практике в подавляющем большинстве случаев единственное жильё остаётся у клиента. Давайте я расскажу подробнее, как это работает по закону, и мы посмотрим вашу ситуацию.»"
        )

    elif text == "🤔 Нужно подумать":
        reply = (
            "🤔 **Нужно подумать**\n\n"
            "**Вариант 1:**\n«Конечно, это важное решение, и я полностью вас понимаю — его нельзя принимать второпях. Чтобы вам было проще всё взвесить, предлагаю не затягивать слишком сильно, потому что долг продолжает расти каждый день. Давайте встретимся или созвонимся в ближайшие дни — я отвечу на все оставшиеся вопросы.»\n\n"
            "**Вариант 2:**\n«Понимаю ваше желание подумать. Пока вы думаете, я могу подготовить для вас подробную информацию именно по вашей ситуации. Скажите, что именно вас больше всего беспокоит?»"
        )

    elif text == "😟 Боюсь последствий":
        reply = (
            "😟 **Боюсь последствий**\n\n"
            "**Вариант 1:**\n«Я вас очень хорошо понимаю. Когда человек впервые сталкивается с банкротством, страх последствий — это абсолютно нормальная реакция. Давайте я честно и спокойно расскажу вам, какие последствия действительно существуют по закону, а какие являются просто мифами.»\n\n"
            "**Вариант 2:**\n«Страх в такой ситуации — это нормально. Большинство наших клиентов вначале тоже боялись. После завершения процедуры почти все говорят, что боялись зря. Давайте разберём вместе, чего именно вы опасаетесь больше всего.»"
        )

    elif text == "💸 Нет денег":
        reply = (
            "💸 **Нет денег**\n\n"
            "**Вариант 1:**\n«Понимаю вас. Сейчас действительно тяжело с финансами, и это одна из главных причин, почему люди откладывают решение. У нас есть гибкая система оплаты и рассрочка. Главное — мы начинаем работу и останавливаем давление коллекторов уже на старте. Давайте посмотрим, какой вариант оплаты будет комфортным именно для вас.»\n\n"
            "**Вариант 2:**\n«Многие клиенты приходят именно с такой проблемой. Мы стараемся подбирать условия так, чтобы первый платёж был минимальным. Самое важное сейчас — остановить рост долга.»"
        )

    # Добавь остальные возражения по аналогии (я могу добавить все сразу, если скажешь)

    else:
        await message.answer("Выберите пункт из меню 👇", reply_markup=main_menu)
        return

    await message.answer(reply, parse_mode="Markdown", reply_markup=vozrazheniya_menu)

if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    print("✅ Бот улучшен: обновлённые тексты + кнопка «⚡ Быстрый ответ»")
    asyncio.run(dp.start_polling(bot))
