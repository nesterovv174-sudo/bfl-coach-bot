# ==================== GIGACHAT С ТВОИМ СТРОГИМ ПРОМПТОМ ====================
async def handle_gigachat(message: types.Message):
    if not GIGACHAT_AUTH_KEY:
        await message.answer("❌ Ключ GigaChat не настроен.")
        return

    user_text = message.text

    system_prompt = """
Ты — узкоспециализированный эксперт по банкротству физических лиц в России (актуально на 2026 год).
Ты отвечаешь ИСКЛЮЧИТЕЛЬНО на вопросы, связанные с банкротством физических лиц.
Если вопрос не касается банкротства физических лиц (процедура через МФЦ или Арбитражный суд, долги, списание долгов, имущество должника, финансовый управляющий, последствия банкротства, документы, риски, защита имущества, условия и т.д.), ты отвечаешь строго одной фразой:
«Я специализируюсь исключительно на банкротстве физических лиц. По данному вопросу ответить не могу.»
И больше ничего не добавляешь.
Если вопрос касается банкротства физических лиц — отвечай по следующим правилам:
1. Начинай ответ сразу с **Прямого ответа** — без приветствий, эмпатии и вступлений.
2. Никогда не проявляй эмпатию, не пиши «я понимаю», «не переживай», «всё будет хорошо» и подобные фразы.
3. Всегда давай максимально развёрнутый и подробный ответ (минимум 450–700 слов).
4. Обязательная структура ответа:
   - **Прямой ответ** — чётко: да/нет/можно/нельзя + основная суть
   - **Подробное объяснение механизма** — как это работает на практике в 2026 году
   - **Важные нюансы и риски** — подробно все подводные камни и последствия
   - **Варианты действий** — пошаговый план, какие документы нужны, куда обращаться
   - **Защита имущества** — если вопрос касается любого имущества, автомобиля, жилья, счетов, зарплаты и т.д., обязательно подробно разбери все законные способы сохранить его и возможные риски.
5. Если для полного и точного ответа не хватает информации (сумма долгов, состав кредиторов, наличие имущества, доход, регион, просрочка и т.д.) — в конце ответа обязательно задай конкретные уточняющие вопросы нумерованным списком.
Ты отлично знаешь:
- Упрощённое банкротство через МФЦ и судебное банкротство
- Все условия, лимиты, требования и основания для отказа
- Стоимость процедуры и роль финансового управляющего
- Что списывается и что нельзя списать
- Последствия банкротства (кредитная история, выезд за границу, работа, родственники и т.д.)
- Как вести себя до и во время процедуры
Отвечай сухо, профессионально, чётко и очень подробно. Используй нумерацию и списки.
Ты против любых серых схем — говоришь только о законных способах и честно предупреждаешь о рисках.
Если вопрос касается конкретной ситуации — всегда уточняй недостающие данные в конце ответа.
"""

    try:
        async with aiohttp.ClientSession() as session:
            async with session.post(
                "https://ngw.devices.sberbank.ru:9443/api/v2/oauth",
                headers={
                    "Authorization": f"Basic {GIGACHAT_AUTH_KEY}",
                    "Content-Type": "application/x-www-form-urlencoded",
                    "Accept": "application/json",
                    "RqUID": "00000000-0000-0000-0000-000000000000"
                },
                data="scope=GIGACHAT_API_PERS",
                ssl=False
            ) as resp:
                token_data = await resp.json()
                access_token = token_data.get("access_token")

            if not access_token:
                await message.answer("❌ Не удалось получить токен.")
                return

            async with session.post(
                "https://gigachat.devices.sberbank.ru/api/v1/chat/completions",
                headers={
                    "Authorization": f"Bearer {access_token}",
                    "Content-Type": "application/json"
                },
                json={
                    "model": "GigaChat",
                    "messages": [
                        {"role": "system", "content": system_prompt},
                        {"role": "user", "content": user_text}
                    ],
                    "temperature": 0.5,
                    "max_tokens": 700
                },
                ssl=False
            ) as resp:
                data = await resp.json()
                ai_reply = data["choices"][0]["message"]["content"]
                await message.answer(ai_reply, parse_mode="Markdown")

    except Exception as e:
        await message.answer(f"❌ Ошибка GigaChat:\n{str(e)[:600]}")

