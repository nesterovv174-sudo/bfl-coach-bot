# ==================== GIGACHAT — МЯГКИЙ ПРОМПТ ====================
async def handle_gigachat(message: types.Message):
    if not GIGACHAT_AUTH_KEY:
        await message.answer("❌ Ключ GigaChat не настроен.")
        return

    try:
        with open("rules.txt", "r", encoding="utf-8") as f:
            rules_content = f.read().strip()
    except FileNotFoundError:
        rules_content = "Правила не найдены."

    user_text = message.text

    system_prompt = f"""
Ты — доброжелательный, спокойный и поддерживающий коуч по банкротству физических лиц.

Твой стиль общения — мягкий, человечный, эмпатичный. Говори как заботливый наставник.

**Строгие правила:**
- Используй информацию ТОЛЬКО из файла rules.txt.
- Минимальная сумма долга для судебного банкротства — 250 000 рублей.
- Если в запросе пользователя нет ни слова про имущество — **никогда** не упоминай имущество, автомобиль, жильё, защиту имущества и т.д.
- Если информации недостаточно — мягко попроси уточнить.

=== ПРАВИЛА ИЗ rules.txt ===
{rules_content}
=== КОНЕЦ ПРАВИЛ ===

Структура ответа:
1. Мягкое эмпатичное начало
2. Прямой понятный ответ
3. Расчёт конкурсной массы (простым текстом)
4. Что можно сделать дальше
5. Мягкие уточняющие вопросы

Говори тепло и по-человечески. Не используй сухой язык и LaTeX.
"""

    try:
        async with aiohttp.ClientSession() as session:
            async with session.post(
                "https://ngw.devices.sberbank.ru:9443/api/v2/oauth",
                headers={
                    "Authorization": f"Basic {GIGACHAT_AUTH_KEY}",
                    "Content-Type": "application/x-www-form-urlencoded",
                    "Accept": "application/json",
                    "RqUID": "00000000-0000-0000-0000-000000000000"
                },
                data="scope=GIGACHAT_API_PERS",
                ssl=False
            ) as resp:
                token_data = await resp.json()
                access_token = token_data.get("access_token")

            if not access_token:
                await message.answer("❌ Не удалось получить токен.")
                return

            async with session.post(
                "https://gigachat.devices.sberbank.ru/api/v1/chat/completions",
                headers={"Authorization": f"Bearer {access_token}", "Content-Type": "application/json"},
                json={
                    "model": "GigaChat",
                    "messages": [
                        {"role": "system", "content": system_prompt},
                        {"role": "user", "content": user_text}
                    ],
                    "temperature": 0.5,
                    "max_tokens": 3000
                },
                ssl=False
            ) as resp:
                data = await resp.json()
                ai_reply = data["choices"][0]["message"]["content"]
                await message.answer(ai_reply, parse_mode="Markdown")

    except Exception as e:
        await message.answer(f"❌ Ошибка GigaChat:\n{str(e)[:600]}")

