import logging
import asyncio
import os
from aiogram import Bot, Dispatcher, types
from aiogram.filters import Command
from aiogram.types import ReplyKeyboardMarkup, KeyboardButton

API_TOKEN = os.getenv("API_TOKEN")

bot = Bot(token=API_TOKEN)
dp = Dispatcher()

# ==================== ГЛАВНОЕ МЕНЮ ====================
main_menu = ReplyKeyboardMarkup(
    keyboard=[
        [KeyboardButton(text="📋 Возражения"), KeyboardButton(text="📞 Скрипты")],
        [KeyboardButton(text="⚡ Быстрый ответ"), KeyboardButton(text="👋 Приветствие")],
    ],
    resize_keyboard=True
)

# ==================== МЕНЮ ВОЗРАЖЕНИЙ ====================
vozrazheniya_menu = ReplyKeyboardMarkup(
    keyboard=[
        [KeyboardButton(text="💰 Дорого / Цена"), KeyboardButton(text="🏠 Заберут квартиру")],
        [KeyboardButton(text="🤔 Нужно подумать"), KeyboardButton(text="😟 Боюсь последствий")],
        [KeyboardButton(text="💸 Нет денег"), KeyboardButton(text="📞 Коллекторы звонят")],
        [KeyboardButton(text="🆓 Сам через МФЦ"), KeyboardButton(text="🔄 Уже пробовал")],
        [KeyboardButton(text="🚫 Не доверяю"), KeyboardButton(text="🙈 Стыдно / что скажут")],
        [KeyboardButton(text="⏰ Времени нет"), KeyboardButton(text="⚖️ Уже в суде")],
        [KeyboardButton(text="🔙 Назад в главное меню")],
    ],
    resize_keyboard=True
)

# ==================== МЕНЮ СКРИПТОВ ====================
skripty_menu = ReplyKeyboardMarkup(
    keyboard=[
        [KeyboardButton(text="📞 Скрипт первого разговора")],
        [KeyboardButton(text="🔙 Назад в главное меню")],
    ],
    resize_keyboard=True
)

@dp.message(Command("start"))
async def start_cmd(message: types.Message):
    await message.answer(
        "👋 Добро пожаловать в AI-коуч для менеджеров по БФЛ!\n\n"
        "Выберите нужный раздел:",
        reply_markup=main_menu
    )

@dp.message()
async def handle_message(message: types.Message):
    text = message.text

    if text == "📋 Возражения":
        await message.answer("Выберите возражение клиента:", reply_markup=vozrazheniya_menu)

    elif text == "📞 Скрипты":
        await message.answer("Выберите скрипт:", reply_markup=skripty_menu)

    elif text == "⚡ Быстрый ответ":
        await message.answer(
            "⚡ Режим быстрого ответа включён.\n\n"
            "Просто скопируйте и отправьте сообщение клиента — я предложу 2–3 варианта ответа.",
            reply_markup=main_menu
        )
        return

    elif text == "👋 Приветствие":
        await message.answer("👋 Добро пожаловать! Используйте меню для работы с клиентами.", reply_markup=main_menu)

    elif text == "🔙 Назад в главное меню":
        await message.answer("Главное меню:", reply_markup=main_menu)

    # ==================== СКРИПТ ПЕРВОГО РАЗГОВОРА ====================
    elif text == "📞 Скрипт первого разговора":
        reply = (
            "📞 **Скрипт первого разговора**\n\n"
            "Добрый день / вечер, {ИМЯ КЛИЕНТА}?\n\n"
            "— Это [ваше имя], я помощник юриста компании «БанкротствоГрупп». Вы недавно общались с моей коллегой (Имя сотрудника кол-центра), она передала мне, что у вас сумма кредитов составляет ………… т.р. и вам интересно списание данной суммы. Всё верно?\n\n"
            "Пауза для ответа клиента.\n\n"
            "— Отлично! Мы как раз специализируемся на списании всех кредитов и долгов и уже многим нашим гражданам помогли. Сейчас я уточню у вас пару вопросов, это займёт 5 минут. Удобно сейчас?\n\n"
            "Но перед этим расскажите, из чего эта сумма состоит (банки, МФО, долги по ЖКХ)?"
        )
        await message.answer(reply, parse_mode="Markdown", reply_markup=skripty_menu)

    # ==================== ИСПРАВЛЕННЫЕ ВОЗРАЖЕНИЯ ====================
    elif text == "💰 Дорого / Цена":
        reply = (
            "💰 **Дорого / Цена**\n\n"
            "**Вариант 1:**\n«Понимаю вас очень хорошо — сейчас действительно каждая копейка на счету, и любая дополнительная трата вызывает вопросы. При этом каждый месяц на вас продолжают начисляться проценты и пени, которые часто съедают больше, чем стоит вся процедура. Мы помогаем остановить этот рост уже в первые недели и предлагаем гибкую рассрочку. Давайте вместе посчитаем, как это будет выглядеть именно в вашем случае?»\n\n"
            "**Вариант 2:**\n«Согласен, что сумма кажется большой на первый взгляд. Но давайте посчитаем по-другому: если ничего не делать, через год вы переплатите гораздо больше. Наша работа — полностью закрыть все долги и дать вам спокойную жизнь. Готовы посмотреть варианты оплаты?»"
        )

    elif text == "🏠 Заберут квартиру":
        reply = (
            "🏠 **Заберут квартиру**\n\n"
            "**Вариант 1:**\n«Это один из самых частых страхов. Хочу вас сразу успокоить: единственное жильё, в котором вы прописаны и проживаете, по закону полностью защищено и не может быть реализовано (кроме ипотеки). Мы всегда сначала делаем полный анализ имущества.»\n\n"
            "**Вариант 2:**\n«Я вас прекрасно понимаю — мысль о том, что могут забрать квартиру, очень пугает. На практике в большинстве случаев единственное жильё остаётся у клиента. Давайте разберём вашу ситуацию подробно.»"
        )

    elif text == "🤔 Нужно подумать":
        reply = (
            "🤔 **Нужно подумать**\n\n"
            "**Вариант 1:**\n«Конечно, это серьёзное решение, и я полностью вас понимаю. Чтобы вам было проще принять решение, предлагаю не затягивать, потому что долг продолжает расти. Давайте встретимся или созвонимся в ближайшие дни — я отвечу на все вопросы.»\n\n"
            "**Вариант 2:**\n«Понимаю, что нужно время. Пока вы думаете, я могу подготовить подробную информацию по вашей ситуации. Скажите, что именно вас больше всего беспокоит?»"
        )

    elif text == "😟 Боюсь последствий":
        reply = (
            "😟 **Боюсь последствий**\n\n"
            "**Вариант 1:**\n«Я вас очень хорошо понимаю. Страх перед неизвестным — это нормальная реакция. Давайте я честно расскажу, какие последствия реально есть по закону, а какие являются мифами.»\n\n"
            "**Вариант 2:**\n«Большинство наших клиентов вначале тоже боялись. После процедуры почти все говорят, что боялись зря. Давайте разберём вместе, чего именно вы опасаетесь.»"
        )

    elif text == "💸 Нет денег":
        reply = (
            "💸 **Нет денег**\n\n"
            "**Вариант 1:**\n«Понимаю вас. Сейчас действительно тяжело с финансами. У нас есть гибкая система оплаты и рассрочка. Главное — остановить давление коллекторов уже на старте. Давайте посмотрим комфортный вариант оплаты для вас.»\n\n"
            "**Вариант 2:**\n«Многие клиенты приходят с такой проблемой. Мы стараемся сделать первый платёж минимальным. Самое важное сейчас — остановить рост долга.»"
        )

    elif text == "📞 Коллекторы звонят":
        reply = (
            "📞 **Коллекторы звонят**\n\n"
            "**Вариант 1:**\n«Понимаю, как это неприятно и давит. После начала процедуры коллекторы и приставы обязаны прекратить общение с вами. Мы берём всё общение на себя.»\n\n"
            "**Вариант 2:**\n«Коллекторы — это временно. Как только мы начнём процедуру, давление должно прекратиться по закону.»"
        )

    elif text == "🆓 Сам через МФЦ":
        reply = (
            "🆓 **Сам через МФЦ**\n\n"
            "**Вариант 1:**\n«МФЦ подходит только для самых простых случаев. При сумме долгов больше 500 тысяч часто бывают отказы или затягивание процесса. Мы берём всю работу на себя и доводим дело до результата.»\n\n"
            "**Вариант 2:**\n«Многие начинали через МФЦ, а потом всё равно приходили к нам. Самостоятельно очень легко допустить ошибки. Давайте сравним риски в вашей ситуации.»"
        )

    elif text == "🔄 Уже пробовал":
        reply = (
            "🔄 **Уже пробовал**\n\n"
            "**Вариант 1:**\n«Сочувствую, что предыдущий опыт был неудачным. Расскажите, пожалуйста, с чем именно вы столкнулись? Мы работаем совсем по-другому и всегда сопровождаем клиента до конца.»\n\n"
            "**Вариант 2:**\n«Понимаю ваше недоверие. Давайте я покажу, чем мы отличаемся от тех компаний, с которыми вы имели дело.»"
        )

    elif text == "🚫 Не доверяю":
        reply = (
            "🚫 **Не доверяю**\n\n"
            "**Вариант 1:**\n«Понимаю ваше недоверие. К сожалению, в этой сфере много недобросовестных компаний. Мы работаем официально, по договору, с прозрачными условиями. Могу показать примеры документов и отзывы.»\n\n"
            "**Вариант 2:**\n«Давайте я подробно расскажу, как у нас построена работа.»"
        )

    elif text == "🙈 Стыдно / что скажут":
        reply = (
            "🙈 **Стыдно / что скажут**\n\n"
            "**Вариант 1:**\n«Это очень деликатная тема, я вас понимаю. Многие клиенты вначале чувствуют то же самое. На практике это практически не влияет на повседневную жизнь.»\n\n"
            "**Вариант 2:**\n«Стыд — нормальная эмоция. Главное — ваше спокойствие и будущее без долгов.»"
        )

    elif text == "⏰ Времени нет":
        reply = (
            "⏰ **Времени нет**\n\n"
            "**Вариант 1:**\n«Понимаю, график плотный. Мы можем провести короткую консультацию по телефону или в мессенджере.»\n\n"
            "**Вариант 2:**\n«Многие клиенты начинают именно с 10-15 минутного звонка.»"
        )

    elif text == "⚖️ Уже в суде":
        reply = (
            "⚖️ **Уже в суде**\n\n"
            "**Вариант 1:**\n«Это не критично. Банкротство можно запустить даже при наличии судебных решений.»\n\n"
            "**Вариант 2:**\n«Мы часто работаем с такими случаями и знаем, как правильно действовать.»"
        )

    else:
        await message.answer("Выберите пункт из меню 👇", reply_markup=main_menu)
        return

    await message.answer(reply, parse_mode="Markdown", reply_markup=vozrazheniya_menu)

if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    print("✅ Бот обновлён — все возражения работают")
    asyncio.run(dp.start_polling(bot))
