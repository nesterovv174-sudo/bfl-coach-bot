# ==================== GIGACHAT С УМНЫМ ПРОМПТОМ (коротко или подробно) ====================
async def handle_gigachat(message: types.Message):
    if not GIGACHAT_AUTH_KEY:
        await message.answer("❌ Ключ GigaChat не настроен.")
        return

    user_text = message.text

    system_prompt = """
Ты — высококомпетентный эксперт по банкротству физических лиц в России (2026 год).
Твоя задача — отвечать максимально полезно и точно.

Правила:
- Если вопрос простой и требует короткого ответа — отвечай кратко и по делу (3–8 предложений).
- Если вопрос сложный, касается конкретной ситуации, имущества, рисков или стратегии — давай очень подробный, развёрнутый ответ (минимум 400–700 слов).
- Сам решай, нужна ли подробность. Не спрашивай разрешения.
- Всегда используй структуру:
  1. Прямой ответ (да/нет/можно/нельзя + суть)
  2. Подробное объяснение (если нужно)
  3. Важные нюансы и риски
  4. Пошаговые рекомендации
  5. Защита имущества (если упоминается)

Отвечай профессионально, понятным языком, без лишней эмпатии. 
Если информации недостаточно — в конце ответа задай конкретные уточняющие вопросы нумерованным списком.
"""

    try:
        async with aiohttp.ClientSession() as session:
            async with session.post(
                "https://ngw.devices.sberbank.ru:9443/api/v2/oauth",
                headers={
                    "Authorization": f"Basic {GIGACHAT_AUTH_KEY}",
                    "Content-Type": "application/x-www-form-urlencoded",
                    "Accept": "application/json",
                    "RqUID": "00000000-0000-0000-0000-000000000000"
                },
                data="scope=GIGACHAT_API_PERS",
                ssl=False
            ) as resp:
                token_data = await resp.json()
                access_token = token_data.get("access_token")

            if not access_token:
                await message.answer("❌ Не удалось получить токен.")
                return

            async with session.post(
                "https://gigachat.devices.sberbank.ru/api/v1/chat/completions",
                headers={
                    "Authorization": f"Bearer {access_token}",
                    "Content-Type": "application/json"
                },
                json={
                    "model": "GigaChat",
                    "messages": [
                        {"role": "system", "content": system_prompt},
                        {"role": "user", "content": user_text}
                    ],
                    "temperature": 0.7,
                    "max_tokens": 2000
                },
                ssl=False
            ) as resp:
                data = await resp.json()
                ai_reply = data["choices"][0]["message"]["content"]
                await message.answer(ai_reply, parse_mode="Markdown")

    except Exception as e:
        await message.answer(f"❌ Ошибка GigaChat:\n{str(e)[:500]}")

